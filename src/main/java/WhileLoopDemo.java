public class WhileLoopDemo {
    public static void main(String[] args) {
        int i = 0;
        while (i < 10) {
            System.out.println(i);
            i++;
        }
        int j = 10;
        do {
            System.out.println(j);
            j--;
        }while (j >0);
        System.out.println(i);
    }
    public void newDemoForGit()
    {
        System.out.println("newDemoForGit");
    }
}
